// Main seeding function
async function seedDatabase() {
  try {
    console.log(' Starting database seeding...\n');

    // Connect to MongoDB
    await mongoose.connect(process.env.MONGODB_URI);
    console.log(' Connected to MongoDB\n');

    // Clear existing data (optional - comment out if you want to keep existing data)
    console.log('  Clearing existing data...');
    await State.deleteMany({});
    await District.deleteMany({});
    await MGNREGAData.deleteMany({});
    console.log(' Existing data cleared\n');

    // Seed States
    console.log(' Seeding states...');
    const stateResults = await State.insertMany(statesData);
    console.log(` Inserted ${stateResults.length} states\n`);

    // Seed Districts
    console.log('  Seeding districts...');
    const districtResults = await District.insertMany(districtsData);
    console.log(` Inserted ${districtResults.length} districts\n`);

    // Seed MGNREGA Data
    console.log(' Seeding MGNREGA data...');
    const dataResults = await MGNREGAData.insertMany(mgnregaData);
    console.log(` Inserted ${dataResults.length} MGNREGA records\n`);

    // Display summary
    console.log('');
    console.log(' Database seeding completed!');
    console.log('');
    console.log(`States:        ${stateResults.length}`);
    console.log(`Districts:     ${districtResults.length}`);
    console.log(`MGNREGA Data:  ${dataResults.length}`);
    console.log('\n');

    console.log(' Sample API calls you can test:');
    console.log('GET /api/v1/states/17/districts');
    console.log('GET /api/v1/district/1752/data?month=Dec&year=2024-2025');
    console.log('GET /api/v1/district/1752/history?startDate=2024-11&endDate=2024-12');
    console.log('GET /api/v1/health\n');

    process.exit(0);

  } catch (error) {
    console.error(' Error seeding database:', error);
    process.exit(1);
  }
}

